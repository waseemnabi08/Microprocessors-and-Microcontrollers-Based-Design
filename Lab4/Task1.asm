ORG 100h
.MODEL SMALL
.STACK 200
.DATA
MESSAGE1 DB 'Enter your message: ', 0Dh, 0Ah, '$'
MESSAGE2 DB 0Dh, 0Ah, 'The message you just entered: ', 0Dh, 0Ah, '$'
BUF DB 20, 22 DUP(?)

.CODE
MOV AX, @DATA
MOV DS, AX

LEA DX, MESSAGE1
MOV AH, 09H
INT 21H

MOV AH, 0AH
MOV DX, OFFSET BUF
INT 21H

MOV BH, 00H
MOV BL, BUF[1]
MOV BUF[BX+2], '$'

LEA DX, MESSAGE2
MOV AH, 09H
INT 21H

LEA DX, BUF[2]
MOV AH, 09H
INT 21H

MOV AH, 4CH
INT 21H
END
